#include<iostream>

using namespace std;

int main(){
    int angka_pertama;
    int angka_kedua;
    int kurang;
    int tambah;
    int kali;
    int bagi;
    angka_pertama=2;
    angka_kedua=1;
    kurang=angka_pertama - angka_kedua;
    cout<<kurang<<endl;
    tambah=angka_pertama + angka_kedua;
    cout<<tambah<<endl;
    kali=angka_pertama * angka_kedua;
    cout<<kali<<endl;
    bagi=angka_pertama / angka_kedua;
    cout<<bagi;

    return 0;
}